/**

 * Add two numbers.
 *
 * @param x The first number.
 * @param y The second number.
 * @returns The sum of x and y.
 *
 * @throws Will throw an error if x or y is not a number.
 *
 * @example
 * const sum = add(1, 2);
 *
 *
 */
function add(x: number, y: number): number {
  return x + y
}

add(1, 2)
